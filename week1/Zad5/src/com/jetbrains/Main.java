        char letterB = 'B';
        int valueB = letterB;
        System.out.printf("%s:%d%n", "B", valueB);
        char letterC = 'C';
        int valueC = letterC;
        System.out.printf("%s:%d%n", "C", valueC);
        char letterSmallA = 'a';
        int valueSmallA = letterSmallA;
        System.out.printf("%s:%d%n", "a", valueSmallA);
